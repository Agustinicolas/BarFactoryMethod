public class Mojito implements Trago {
    public void ingredientes(){
        String cadena = 
        "+ Ingredintes del Mojito:\n" + //
        "- 3 limas\n" + //
        "- 100 ml de agua con gas\n" + //
        "- 50 ml ron\n" + //
        "- 3 ramitas de menta\n" + //
        "- 40 grs de azucar\n" + //
        "- Hielo\n";
        System.out.println(cadena);
    }
    public void preparacion(){
        String cadena =
        "+ Receta del Mojito:\n" + //
        "cortar las limas en cuartos, las colocamos en un vaso largo,\n" + //
        "junto con el azucar y las ramas de menta. Se machaca para que suelte el jugo y agregar el ron,\n" + //
        "el hielo y el agua con gas. Finalmente decorar con menta.\n";
        System.out.println(cadena);
    }
}
