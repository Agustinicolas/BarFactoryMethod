public class Daiquiri implements Trago {
    public void ingredientes(){
        String cadena = 
        "+ Ingredintes del Daiquiri:\n" + //
        "- 150 grs de fruta natural: frutillas, durazno o anana\n" + //
        "- Agua\n" + //
        "- 100 ml de vodka o ron\n" + //
        "- 30 grs de azucar\n" + //
        "- Hielo\n" + //
        "- Menta\n";
        System.out.println(cadena);
    }
    public void preparacion(){
        String cadena =
        "+ Receta del Daiquiri:\n" + //
        "colocar la fruta y el azucar en la licuadora,\n" + //
        "agrega el agua, el hielo y el ron o vodka y licuamos.\n" + //
        "Servir y decorar con hojitas de menta.\n";
        System.out.println(cadena);
    }
    
}
